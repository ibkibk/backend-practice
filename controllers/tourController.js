const Tour = require("./../models/tourModel");

exports.addTour = async (req, res) => {
  try {
    const newTour = await Tour.create(req.body);
    res.status(201).json({
      status: "success",
      data: {
        tour: newTour,
      },
    });
  } catch (error) {
    res.status(404).json({
      status: "Fail",
      msg: error,
    });
  }
};

